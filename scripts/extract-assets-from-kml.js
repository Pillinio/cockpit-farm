#!/usr/bin/env node
/**
 * extract-assets-from-kml.js
 *
 * Parses the Erichsfelde KML file and outputs SQL UPSERT statements
 * for the farm_assets table.
 *
 * Usage:
 *   node scripts/extract-assets-from-kml.js > /tmp/assets.sql
 *   node scripts/extract-assets-from-kml.js | psql "$DATABASE_URL"
 */

const fs = require('fs');
const path = require('path');

// --- Configuration -----------------------------------------------------------

const KML_PATH = path.join(
  __dirname,
  '..',
  'Data_Input',
  'Okt. 2025 Erichsfelde NS.kmz.kml'
);

/** Map KML folder names → asset_type values */
const FOLDER_MAP = {
  'Bohrlöcher':                    'borehole',
  'Dams':                          'dam',
  'Mills':                         'windmill',
  'Tränken':                       'trough',
  'Häuser / Orientierungspunkte':  'house',
  'Ansitze':                       'hunting_blind',
};

// --- Helpers -----------------------------------------------------------------

/**
 * Extract all <Folder> blocks that match one of the target folder names,
 * then pull out every <Placemark> that contains a <Point>.
 */
function parseFolderPlacemarks(xml, folderName) {
  const results = [];

  // Find the folder by its <name> tag.
  // We search for the opening <Folder …> that is followed (eventually) by
  // <name>folderName</name>.  We then collect Placemarks until the matching
  // </Folder>.
  const escapedName = folderName.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const folderStartRe = new RegExp(
    `<Folder[^>]*>\\s*<name>${escapedName}</name>`,
    'g'
  );

  let folderMatch;
  while ((folderMatch = folderStartRe.exec(xml)) !== null) {
    const startIdx = folderMatch.index;

    // Find the matching </Folder> — we need to handle nested folders.
    let depth = 1;
    let searchIdx = startIdx + folderMatch[0].length;
    while (depth > 0 && searchIdx < xml.length) {
      const nextOpen = xml.indexOf('<Folder', searchIdx);
      const nextClose = xml.indexOf('</Folder>', searchIdx);

      if (nextClose === -1) break; // malformed, bail

      if (nextOpen !== -1 && nextOpen < nextClose) {
        depth++;
        searchIdx = nextOpen + 7;
      } else {
        depth--;
        if (depth === 0) {
          const folderXml = xml.substring(startIdx, nextClose + '</Folder>'.length);
          extractPlacemarks(folderXml, results);
        }
        searchIdx = nextClose + '</Folder>'.length;
      }
    }
  }

  return results;
}

function extractPlacemarks(folderXml, results) {
  const placemarkRe = /<Placemark[\s\S]*?<\/Placemark>/g;
  let pm;
  while ((pm = placemarkRe.exec(folderXml)) !== null) {
    const block = pm[0];

    // Only interested in Point placemarks (skip LineString, Polygon, etc.)
    if (!/<Point>/.test(block)) continue;

    const nameMatch = block.match(/<name>([^<]+)<\/name>/);
    const coordMatch = block.match(/<coordinates>\s*([^<]+?)\s*<\/coordinates>/);

    if (!nameMatch || !coordMatch) continue;

    const name = nameMatch[1].trim();
    const parts = coordMatch[1].split(',');
    if (parts.length < 2) continue;

    const lng = parseFloat(parts[0]);
    const lat = parseFloat(parts[1]);

    if (isNaN(lng) || isNaN(lat)) continue;

    results.push({ name, lng, lat });
  }
}

function escapeSQL(str) {
  return str.replace(/'/g, "''");
}

// --- Main --------------------------------------------------------------------

function main() {
  if (!fs.existsSync(KML_PATH)) {
    console.error(`ERROR: KML file not found at ${KML_PATH}`);
    process.exit(1);
  }

  const xml = fs.readFileSync(KML_PATH, 'utf-8');
  const lines = [];

  lines.push('-- Auto-generated by extract-assets-from-kml.js');
  lines.push(`-- Source: ${path.basename(KML_PATH)}`);
  lines.push(`-- Generated: ${new Date().toISOString()}`);
  lines.push('');
  lines.push('BEGIN;');
  lines.push('');

  let totalCount = 0;

  for (const [folderName, assetType] of Object.entries(FOLDER_MAP)) {
    const placemarks = parseFolderPlacemarks(xml, folderName);

    if (placemarks.length === 0) {
      lines.push(`-- WARNING: No placemarks found for folder "${folderName}"`);
      lines.push('');
      continue;
    }

    lines.push(`-- ${folderName} (${assetType}): ${placemarks.length} assets`);

    for (const { name, lng, lat } of placemarks) {
      lines.push(
        `INSERT INTO farm_assets (asset_type, name, location)` +
        ` VALUES ('${assetType}', '${escapeSQL(name)}',` +
        ` ST_SetSRID(ST_MakePoint(${lng}, ${lat}), 4326))` +
        ` ON CONFLICT (farm_id, asset_type, name)` +
        ` DO UPDATE SET location = EXCLUDED.location;`
      );
    }

    lines.push('');
    totalCount += placemarks.length;
  }

  lines.push('COMMIT;');
  lines.push('');
  lines.push(`-- Total: ${totalCount} assets`);

  console.log(lines.join('\n'));
}

main();
